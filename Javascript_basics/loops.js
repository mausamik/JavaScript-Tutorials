/*

Types of Loops 
    for
    while
    do-while
    for-in 
*/

// for 
console.log("for loop")
for(i = 1 ; i < 5 ; i++)
{
    console.log(i + " " )
}


console.log("while loop")
var j = 1 
while(j < 5 ){
    console.log(j+ " ");
    j++;
}


console.log("do while loop")
var k = 1 
do{
    console.log(k + " ");
    k++;

}while(k< 5);